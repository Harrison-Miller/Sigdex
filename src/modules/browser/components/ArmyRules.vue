.lore-dropdown {
  margin-bottom: 1em;
  font-size: 1.1em;
  padding: 0.4em 1em;
  border-radius: 4px;
  border: 1.5px solid #222;
  background: #f9f9f9;
}
.section-subheader {
  text-align: left;
}
</style>
